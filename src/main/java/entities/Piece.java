package entities;

public class Piece {

	private Integer row;
	private Integer column;
	
	public Piece()
	{
		
	}

	/**
	 * @return the row
	 */
	public Integer getRow() 
	{
		return row;
	}

	/**
	 * @param row the row to set
	 */
	public void setRow(Integer row)
	{
		this.row = row;
	}

	/**
	 * @return the column
	 */
	public Integer getColumn() 
	{
		return column;
	}

	/**
	 * @param column the column to set
	 */
	public void setColumn(Integer column) 
	{
		this.column = column;
	}
	

	
}
